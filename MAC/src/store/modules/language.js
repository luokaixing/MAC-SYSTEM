const state = {
  language: [],
  LAN: {},
  languageList: []
};

const mutations = {
  CHANGE_LANGUAGE(state, id) {
    let LAN = state.languageList.filter(e => +e.id === +id);
    if (LAN.length) {
      state.LAN = LAN[0].fields;
    } else {
      state.LAN = state.languageList[0].fields;
    }
  },
  ADD_LANGUAGE(state, language) {
    state.languageList = [...state.languageList, ...[language]];
  },

  SET_LANGUAGE_LIST(state, languageList) {
    state.languageList = languageList;
    state.LAN = languageList[0].fields;
  },

  DROP_LANGUAGE(state, id) {
    state.languageList = state.languageList.filter(e => {
      return e.id !== id;
    });
  },

  TRIGGLER_LANGUAGE(state, { id, enable }) {
    state.languageList.filter(e => {
      if (e.id === id) {
        e.status = enable ? 1 : 0;
      }
    });
  },
  ADD_FILED(state, name) {
    state.languageList.forEach(e => {
      e.fields[name] = "";
    });
  }
};

const actions = {
  trigglerLanguage({ commit }, id) {
    commit("CHANGE_LANGUAGE", id);
  },
  addLanguage({ commit }, language) {
    commit("ADD_LANGUAGE", language);
  },
  setLanguageList({ commit }, languageList) {
    commit("SET_LANGUAGE_LIST", languageList);
  },
  dropLanguage({ commit }, id) {
    commit("DROP_LANGUAGE", id);
  },
  trigglerLanguageStatus({ commit }, obj) {
    commit("TRIGGLER_LANGUAGE", obj);
  },
  addFiled({ commit }, name) {
    commit("ADD_FILED", name);
  }
};

export default {
  namespaced: true,
  state,
  mutations,
  actions
};
